class Novel {
  final String id;
  final String title;
  final String author;
  final double rating;
  final int reviews;
  final String genre;
  final int chapters;
  final String coverImage;

  Novel({
    required this.id,
    required this.title,
    required this.author,
    required this.rating,
    required this.reviews,
    required this.genre,
    required this.chapters,
    required this.coverImage,
  });
}